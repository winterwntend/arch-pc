SECTION .data
    msg: DB 'Введите строку: ', 0     ; Сообщение для вывода
    len: EQU $ - msg                   ; Длина сообщения

SECTION .bss
    buf1: RESB 80                      ; Буфер для ввода строки

SECTION .text
    GLOBAL _start

_start:
    ; Вывод сообщения
    mov eax, 4                         ; Системный вызов для записи (sys_write)
    mov ebx, 1                         ; Дескриптор файла 1 (stdout)
    mov ecx, msg                       ; Адрес сообщения
    mov edx, len                       ; Длина сообщения
    int 0x80                           ; Вызов ядра

    ; Чтение строки
    mov eax, 3                         ; Системный вызов для чтения (sys_read)
    mov ebx, 0                         ; Дескриптор файла 0 (stdin)
    mov ecx, buf1                      ; Адрес буфера для ввода
    mov edx, 80                        ; Максимальная длина ввода
    int 0x80                           ; Вызов ядра

    ; Вывод введённой строки
    mov eax, 4                         ; Системный вызов для записи (sys_write)
    mov ebx, 1                         ; Дескриптор файла 1 (stdout)
    mov ecx, buf1                      ; Адрес буфера
    mov edx, 80                        ; Длина буфера (здесь лучше учитывать фактическую длину)
    int 0x80                           ; Вызов ядра

    ; Завершение программы
    mov eax, 1                         ; Системный вызов для выхода (sys_exit)
    xor ebx, ebx                       ; Код возврата 0
    int 0x80                           ; Вызов ядра
